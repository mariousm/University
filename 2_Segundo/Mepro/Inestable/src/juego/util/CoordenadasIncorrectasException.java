public class CoordenadasIncorrectasException extends Exception {

	public CoordenadasIncorrectasException() {
		super();
	}

	public CoordenadasIncorrectasException(String arg) {
		super(arg);
	}

	public CoordenadasIncorrectasException(Throwable arg) {
		super(arg);
	}

	public CoordenadasIncorrectasException(String arg, Throwable arg1) {
		super(arg, arg1);
	}

	public CoordenadasIncorrectasException(String arg, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg, arg1, arg2, arg3);
	}
}
